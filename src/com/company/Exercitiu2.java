package com.company;

public class Exercitiu2 {


    public static void main(String[] args) {

        System.out.println("Rezultatul adunarii este: " + adunare(212, 123));
        System.out.println("Rezultatul scaderii este: " + scadere(2129, -32));
        System.out.println("Rezultatul inmultirii este: " + inmultiree(123, 3));
        System.out.println("Rezultatul iimpartiri este: " + (24 / 3));
    }
    public static int adunare(int primulNumar, int alDoileaNumar) {

        int rezultat = primulNumar + alDoileaNumar;

        return rezultat;

    }

    public static int scadere(int primulNumar, int alDoileaNumar) {

        int rezultat = primulNumar + alDoileaNumar;

        return rezultat;

    }

    public static int inmultiree(int primulNumar, int alDoileaNumar) {

        int rezultat = primulNumar * alDoileaNumar;

        return rezultat;
    }

    public static int impartire(int primulNumar, int alDoileaNumar) {

        int rezultat = primulNumar + alDoileaNumar;

        return rezultat;
    }
}




